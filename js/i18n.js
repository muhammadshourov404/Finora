let currentLang = localStorage.getItem('finora_lang') || 'en';

function t(key){
  return (translations[currentLang] && translations[currentLang][key]) || key;
}

function applyTranslations(){
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    if(translations[currentLang][key]) el.textContent = translations[currentLang][key];
  });
  document.getElementById('htmlRoot').lang = currentLang;
  document.body.classList.toggle('lang-bn', currentLang === 'bn');
}

function setLang(lang){
  currentLang = lang;
  localStorage.setItem('finora_lang', lang);
  document.getElementById('lang-en')?.classList.toggle('active', lang==='en');
  document.getElementById('lang-bn')?.classList.toggle('active', lang==='bn');
  applyTranslations();
  if(typeof renderCurrentPage === 'function') renderCurrentPage();
}
